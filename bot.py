@bot.event
async def on_member_join(member: discord.Member):
    channel_id = config.get("welcome_channel")
    if not channel_id:
        return
    channel = member.guild.get_channel(int(channel_id))
    if not channel:
        return
    msg = fmt_placeholder(config["welcome_message"], member)
    try:
        await channel.send(msg)
    except discord.HTTPException:
        pass

    # \u2500\u2500 Log: member join \u2500\u2500
    embed = discord.Embed(
        title="\ud83d\udce5 Member Joined",
        color=discord.Color.green(),
        timestamp=discord.utils.utcnow()
    )
    embed.set_thumbnail(url=member.display_avatar.url)
    embed.add_field(name="User",       value=f"{member} (`{member.id}`)", inline=False)
    embed.add_field(name="Account Age",value=f"<t:{int(member.created_at.timestamp())}:R>", inline=False)
    embed.set_footer(text=f"Member #{member.guild.member_count}")
    await send_log(embed)


@bot.event
async def on_member_remove(member: discord.Member):
    embed = discord.Embed(
        title="\ud83d\udce4 Member Left",
        color=discord.Color.red(),
        timestamp=discord.utils.utcnow()
    )
    embed.set_thumbnail(url=member.display_avatar.url)
    embed.add_field(name="User",   value=f"{member} (`{member.id}`)", inline=False)
    embed.add_field(name="Joined", value=f"<t:{int(member.joined_at.timestamp())}:R>" if member.joined_at else "Unknown", inline=False)
    await send_log(embed)


